<?php

 class Pessoa
 {

    private $nome;
    private $idade;


    //funções da classe:

    public function getNome()
    {
        return $this->nome;
    }

    public function setNome($nome)
    {
        $this->nome = $nome;
        return $this;
    }

    public function getIdade()
    {
        return $this->idade;
    }

    public function setIdade($idade)
    {
        $this->idade = $idade;
        return $this;
    }
 }//fim da classe



?>
